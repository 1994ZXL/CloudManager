public class Travel {
    private String name;
    private String travleTime;
    private String travleState;

    public Travel() {

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTravleTime() {
        return travleTime;
    }

    public void setTravleTime(String travleTime) {
        this.travleTime = travleTime;
    }

    public String getTravleState() {
        return travleState;
    }

    public void setTravleState(String travleState) {
        this.travleState = travleState;
    }
}
